package FlujoDeControl.Bucles;

public class For {
    public static void main(String[] args) {
        //variable inicializadora ; condicion ; incremento o decremento
        for (int i=0;i<10;i++){
            System.out.println(i);
        }
        System.out.println("=========================");
        for (int i=10;i>=0;i--){
            System.out.println(i);
        }

    }
}
