package FlujoDeControl.Bucles;

import java.util.Scanner;

public class While {
    public static void main(String[] args) {
        int i = 1;//variable inicializadora

        //condicion
        while (i <= 5) {
            //ejecutable
            System.out.println("i = " + i);
//            incremento
            i++;
        }
    }
}
